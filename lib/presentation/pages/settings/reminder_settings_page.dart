            hour: _doNotDisturbEndTime! ~/ 60,
            minute: _doNotDisturbEndTime! % 60,
          )
        : null;

    final result = await showDialog<Map<String, TimeOfDay?>>(
      context: context,
      builder: (context) {
        return StatefulBuilder(
