                        setState(() {
                          startTime = time;
                        });
                      }
                    },
                  ),
                  ListTile(
                    title: const Text('ÁªìÊùüÊó∂Èó¥'),
                    subtitle: Text(endTime?.format(context) ?? 'Êú™ËÆæÁΩÆ'),
                    trailing: const Icon(Icons.access_time),
                    onTap: () async {
                      final time = await showTimePicker(
                        context: context,
                        initialTime: endTime ?? const TimeOfDay(hour: 7, minute: 0),
                      );
                      if (time != null) {
                        setState(() {
                          endTime = time;
                        });
                      }
                    },
                  ),
                  SizedBox(height: 16.h),
                  TextButton(
                    onPressed: () {
                      setState(() {
